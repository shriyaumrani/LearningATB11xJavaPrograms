package ex_09_Switch;

public class Lab074_interview {
    public static void main(String[] args) {
        int a = 10;
        switch (a){
            case 5:
                System.out.println(" The value is 5");
                break;
            case 10:
                System.out.println("The value is 10");
                break;
            default:
                System.out.println("invalid");

        }
    }
}
