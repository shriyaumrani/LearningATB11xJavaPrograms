package ex_10_For_Loop;

public class Lab087_For_If_Loop {
    public static void main(String[] args) {
        
        for(int shriya = 0; shriya<18; shriya++){
            if(shriya>15){
                System.out.println("Gift from dad");
            }else{
                System.out.println("No gift");
            }

        }
    }
}
