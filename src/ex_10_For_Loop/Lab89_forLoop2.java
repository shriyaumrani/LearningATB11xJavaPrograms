package ex_10_For_Loop;

public class Lab89_forLoop2 {
    public static void main(String[] args) {
        for(long i = 1l;i<10;i++){ // can have long
            System.out.println(i);
        }
        for(float f = 0.0f;f<10.67;f++){
            System.out.println("Hi float -> " + f); // can have float values
        }

    }
}
