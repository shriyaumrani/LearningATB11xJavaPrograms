package ex_09_Switch;

public class Lab081_interview {
    public static void main(String[] args) {
        char code = 'C';
        switch(code){
            case 'A':
                System.out.println("Hello");
                break;
            case 'B':
                System.out.println("Hi");
                break;
            default:
                System.out.println("none");
                break;
        }
    }
}
