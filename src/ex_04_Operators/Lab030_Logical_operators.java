package ex_04_Operators;

public class Lab030_Logical_operators {
    public static void main(String[] args) {
        boolean c = true;
        System.out.println(!c);
        System.out.println(!!c);
        boolean a = true || false;
        boolean b = true && false;
        System.out.println(a);
        System.out.println(b);
    }
}
