package ex_07_Increament_Decreament_op;

public class Lab055_ID_OP {
    public static void main(String[] args) {
         int a = 10;
         int b = ++a;
        System.out.println(b);
        System.out.println(a);

        // Exp and result
        // Line no | a|m result b
        // 5 | 10 |NA
        //6 | 11 | 11
        //7 | 11(NA) | 11
        //8 |11|11(NA)
    }
}
